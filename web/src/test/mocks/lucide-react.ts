import * as React from 'react';

// Create a proxy handler that returns a mock component for any icon
const handler = {
  get: (_: unknown, prop: string) => {
    // Skip non-icon properties
    if (prop === 'then' || typeof prop !== 'string' || prop.startsWith('_')) {
      return undefined;
    }
    
    // Return a mock component for any icon
    return () => React.createElement('div', { 'data-testid': `icon-${prop}` }, prop);
  }
};

// Create a proxy object that will handle all icon requests
const iconProxy = new Proxy({}, handler);

// Export the proxy as the default export and also as named exports
export default iconProxy;

